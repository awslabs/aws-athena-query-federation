package com.amazonaws.athena.connectors.cloudera;

import com.amazonaws.athena.connector.credentials.DefaultCredentials;
import com.amazonaws.athena.connector.credentials.DefaultCredentialsProvider;

import java.util.Map;

public class HiveCredentialsProvider extends DefaultCredentialsProvider
{
    public HiveCredentialsProvider(final String secretString)
    {
        super(secretString);
    }

    @Override
    public Map<String, String> getCredentialMap()
    {
        DefaultCredentials creds = getCredential();
        return Map.of(
                HiveConstants.USER, creds.getUser(),
                HiveConstants.PASSWORD, creds.getPassword()
        );
    }
}
